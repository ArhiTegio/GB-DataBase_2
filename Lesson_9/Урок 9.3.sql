--Создайте хранимую функцию hello(), которая будет возвращать приветствие, в зависимости от текущего времени суток. 
--С 6:00 до 12:00 функция должна возвращать фразу "Доброе утро", с 12:00 до 18:00 функция должна возвращать фразу "Добрый день", 
--с 18:00 до 00:00 — "Добрый вечер", с 00:00 до 6:00 — "Доброй ночи".

CREATE PROCEDURE hello()
BEGIN
	IF  TIME(NOW()) BETWEEN '6:00:00' AND '12:00:00' THEN SELECT 'Доброе утро';
	ELSEIF TIME(NOW()) BETWEEN '12:00:00' AND '18:00:00' THEN SELECT 'Добрый день';
	ELSE SELECT 'Доброй ночи';
	END IF;	
END;

CALL hello();

В таблице products есть два текстовых поля: name с названием товара и description с его описанием. Допустимо присутствие обоих полей 
или одно из них. Ситуация, когда оба поля принимают неопределенное значение NULL неприемлема. Используя триггеры, добейтесь того, чтобы 
одно из этих полей или оба поля были заполнены. При попытке присвоить полям NULL-значение необходимо отменить операцию.

CREATE TRIGGER check_product_insert BEFORE INSERT ON shop.products 
FOR EACH ROW 
BEGIN 
	IF (NEW.name IS NULL AND NEW.desription IS NULL) THEN SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = 'INSERT cancel. Name and description is NULL';		
	END IF;
END;

CREATE TRIGGER check_product_update BEFORE UPDATE ON shop.products 
FOR EACH ROW 
BEGIN 
	IF (NEW.name IS NULL AND NEW.desription IS NULL) THEN SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = 'UPDATE cancel. Name and description is NULL';
	END IF;
END;

